import UIKit

enum MainMenuItem: String, MenuItemProtocol {
    case allControls
    case label
    case button
	case textField
	case textView
    
    var name: String {
		switch self {
		case .allControls:
			return rawValue.autoGeneratedMenuItemName
		case .label:
			return rawValue.autoGeneratedMenuItemName
		case .button:
			return rawValue.autoGeneratedMenuItemName
		case .textField:
			return "TextField"
		case .textView:
			return "TextView"
		}
    }
    
    var viewController: UIViewController? {
        switch self {
        case .allControls:
            return AllControlsViewController()
        case .label:
            return LabelMenuViewController()
        case .button:
            return ButtonMenuViewController()
		case .textField:
			return TextFieldMenuViewController()
		case .textView:
			return TextViewMenuViewController()
        }
    }
}
